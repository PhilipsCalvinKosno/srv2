				<h2>Toppalada</h2>
					<p>	Toppalada adalah kuliner KHAS dari Sulawesi Selatan yang terbuat dari daging sapi.
                    </p>

					<br>
					<h3>Bahan-bahan:</h3>
					<ul>
						<li> 1 kg daging sapi bagian paha</li>
			  <li> 2 lembar daun salam</li>
			    <li> 3 lembar daun jeruk</li>
			    <li> 1 jempol lengkuas, geprek</li>
			    <li> 1 batang serai, geprek</li>
			      <li> 2 sdm bubuk cabai (opsional)</li>
			      <li> Segenggam cabai rawit merah domba utuh</li>
			      <li> Air asam dari 1 mata asam jawa yang dicairkan</li>
			    <li> Garam</li>
			    <li> Kaldu jamur bubuk</li>
			      <li> Air matang</li>
			      <li> Minyak goreng</li>
					</ul>

					<br>
					<h3>Bahan bumbu halus:</h3>
					<ol>

					<li> 10 bawang merah</li>
					  <li> 7 bawang putih</li>
					<li> 100 gram cabai merah keriting</li>
					<li> 3 butir kemiri utuh</li>
					</ol>

					<br>
					<h3>Cara Memasak:</h3>
					<ol>
						<li> Cuci bersih daging. Potong sesuai selera.</li>
						<li> Panaskan sedikit minyak goreng. Tumis bumbu halus dan cabai bubuk hingga harum.</li>
						<li> Masukkan daun salam, daun jeruk, lengkuas dan serai. Aduk rata.</li>
						<li> Masukkan potongan daging. Aduk rata hingga berubah warna. Masukkan air panas hingga daging terendam.</li>
						<li> Aduk rata dan masak menggunakan api kecil. Aduk sesekali.</li>
						<li> Saat air mulai surut masukkan air asam jawa, cabai rawit domba, garam dan kaldu jamur. Aduk rata.</li>
						<li> Tes rasa. Masak hingga bumbu mengental. Angkat dan sajikan.</li>
					</ol>


