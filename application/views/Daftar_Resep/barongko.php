			<h2>Barongko</h2>
			<p>Barongko merupakan makanan khas Bugis-Makassar yang terbuat dari pisang yang dihaluskan, telur, santan, gula pasir, dan garam. Kemudian dibungkus daun pisang lalu dikukus. Jika sudah matang, dimasukkan ke dalam kulkas. Dahulu, Barongko disajikan sebagai hidangan penutup bagi para raja Bugis.
            </p>

			<br>
			<h3>Bahan-bahan:</h3>
			<ul>

				    <li>1 sisir pisang kepok matang</li>
				  <li>100 gr gula pasir</li>
				    <li>350 ml santan</li>
				  <li>4 butir telur</li>
				  <li> 1 saset susu bubuk full cream(bisa diganti 2 saset SKM)</li>
				  <li> 1/2 sdt garam</li>
				  <li>Secukupnya daun pisang untuk membungkus</li>
				  <li> Lidi untuk menyemat</li>
			</ul>





				<br>
				<h3>Cara Memasak:</h3>
				<ol>
					<li>Kupas pisang, iris daging buahnya, buang bagian tengahnya yang berwarna hitam.</li>
				<li> Masukkan dalam blender bersama santan, gula pasir, susu dan garam lalu blender sampai halus.</li>
				<li> Matikan blender, masukkan telur kemudian blender lagi sebentar sampai tercampur rata.</li>
				<li> Ambil selembar daun pisang, tuang 1 sendok sayur adonan lalu bungkus dengan bentuk tum. Semat dengan lidi, kukus selama 20 menit.</li>
				  <li> Angkat dan biarkan dingin. Kemudian masukkan lemari es. Sajikan dingin lebih nikmat.</li>
				</ol>

