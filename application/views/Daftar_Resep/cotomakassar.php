			<h2>Coto Makassar</h2>
			<p>Coto makassar atau coto mangkasara adalah makanan tradisional Makassar, Sulawesi Selatan. Makanan ini terbuat dari jeroan sapi yang direbus dalam waktu yang lama. Rebusan jeroan bercampur daging sapi ini kemudian diiris-iris lalu dibumbui dengan bumbu yang diracik secara khusus.
			</p>

			<br>
			<h3>Bahan-bahan:</h3>
			<ul>
				<li>500 gr daging sapi</li>
			<li>  500 gr jeroan sapi</li>
				<li>2,5 liter air cucian beras yang kedua</li>
				<li>3 batang serai</li>
			<li>  4 lembar daun salam</li>
			<li>  2 lembar daun jeruk purut</li>
			<li>  Kayu manis 5 cm</li>
			</ul>

			<br>
			<h3>Bumbu Halus:</h3>
			<ul>
				<li>  10 siung bawang merah</li>
				<li>  8 siung bawang putih</li>
				<li>  6 butir kemiri sangrai</li>
				<li>  2 sdm ketumbar sangrai</li>
				<li>  1 sdm jintan sangrai</li>
				<li>  1 sdt butir merica</li>
				<li>  1/2 sdt pala</li>
				<li>  Seruas jahe</li>
				<li>  3 buah cabai merah besar buang bijinya</li>
				<li>  4 batang serai, potong-potong tipis</li>
				<li>  2 rimpang lengkuas muda, potong-potong tipis.</li>
				<li>  Minyak secukupnya</li>
			</ul>
			<br>

			<h3>Bahan lain:</h3>
			<ol>

			<li>  100 gr kacang tanah disangrai, haluskan</li>
			<li>  Kaldu bubuk sapi secukupnya</li>
			<li>  Garam secukupnya</li>
			<li>  Gula pasir secukupnya</li>
			</ol>
			<br>
			<h3>Pelengkapnya:</h3>
			<ol>

			<li>  Ketupat mini</li>
			<li>  Daun bawang</li>
			<li>  Daun seledri</li>
			<li>  Bawang goreng</li>
			<li>  Sambal</li>
			  <li>Jeruk nipis</li>
			<li>  Kecap manis/asin</li>
			</ol>


			<br>
			<h3>Cara Memasak:</h3>
			<ol>
				<li>  Cuci bersih daging dan jeroan, kemudian rebus daging dengan air beras. Masukkan serai geprek, daun jeruk, daun salam, dan kayu manis.</li>
				<li>  Jeroan rebus terpisah dengan air biasa, setelah sudah empuk baru gabung dengan daging bersama kaldunya.</li>
				<li>  Masukan bumbu halus yang sudah ditumis dan kacang tanah yang sudah diblender, lalu aduk rata.</li>
				<li>  Beri gula, garam dan kaldu bubuk, cek rasa. Masak hingga matang mendidih, daging dan jeroan matang meresap.</li>
				<li>  Angkat daging dan jeroan dari dalam kuah, potong-potong sesuai selera.</li>
				<li>  Dalam mangkuk, tata potong daging dan jeroan, siram dengan kuah coto. Taburi daun bawang daun seledri dan bawang goreng. Kemudian hidangkan dengan ketupat. Beri sambal dan jeruk nipis.</li>
			</ol>

