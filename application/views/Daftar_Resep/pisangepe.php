						<h2>Pisang Epe</h2>
					<p>Dalam bahasa Makassar, Epe memiliki arti jepit. Dalam arti jajanan Pisang Epe adalah sebuah pisang raja, yang dibakar dan dijepit hingga pipih. Pemilihan pisang untuk jajanan ini, memang menggunakan pisang raja. Hal ini karena pisang raja memiliki aroma yang khas dan rasa yang cenderung ke rasa manis.
                    </p>

					<br>
					<h3>Bahan-bahan:</h3>
					<ul>
						<li>1 sisir pisang kepok matang</li>
							<li>3 bulatan kecil gula merah</li>
						<li>200 ml air</li>
						<li> Sejumput garam</li>
						<li> 2 sdm daging durian</li>
					</ul>






					<br>
					<h3>Cara Memasak:</h3>
					<ol>
						<li> Rendam gula merah dengan air hingga semua gula leleh, beri garam dan daging durian.</li>
					    <li> Masak dengan api sedang hingga agak kental, sisihkan.</li>
					  <li> Kupas pisang, panggang bolak-balik di teflon hingga ke dua sisi pisang tampak memar, angkat, dan letakkan di atas piring ceper.</li>
					  <li> Kemudian tekan dengan bagian bawah piring ceper lain hingga pisang jadi gepeng.</li>
					  <li> Tata di piring lalu beri topping gula merah duren.</li>
					</ol>

