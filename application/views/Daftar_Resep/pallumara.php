					<h2>Pallumara</h2>
					<p>Pallumara berasal dari kata“pallu” yang berarti masak dan “mara” yang berarti kering dalam hal ini kental.Secara keseluruhan pallumara adalah makanan olahan ikan yang dimasak dengah yang kental.

                    </p>

					<br>
					<h3>Bahan-bahan:</h3>
					<ul>
						<li>500 gr ikan layang (boleh ikan apa saja)</li>
					<li>1 1/2 sdm asam jawa</li>
						<li> 1/2 sdm kunyit bubuk</li>
						<li> 1 sdm garam</li>
						<li>1 1/2sdm gula pasir</li>
						<li> 1 liter air</li>
					</ul>



					<br>
					<h3>Cara Memasak:</h3>
					<ol>
						<li> Bersihkan ikan, lalu lumuri jeruk nipis, diamkan 10-15 menit.</li>
					<li>Cuci bersih dan masukkan dalam panci.</li>
						<li>Larutkan asam jawa dalam air, saring, dan tuang ke dalam panci berisi ikan.</li>
						<li> Tambahkan kunyit bubuk, aduk rata.</li>
					<li> Masak ikan sampai mendidih, masukkan gula garam, tes rasa, angkat.</li>
						<li>Sajikan dengan racak mangga.</li>
                    </ol>



