		<h2>Ayam Nasi Palleko</h2>
			<p>Ada banyak sekali makanan khas dari berbagai daerah di Indonesia, salah satunya adalah ayam palekko dari Bugis. Sekilas mirip kare atau opor tapi dengan citarasa yang berbeda.
			</p>

			<br>
			<h3>Bahan-bahan:</h3>
			<ul>
				<li>1 ekor ayam, potong kecil-kecil</li>
				<li>50 ml air asam kental</li>
				<li> 1 batang serai geprek</li>
				<li>1 cm lengkuas geprek</li>
				<li>2 lembar daun salam</li>
					<li> 1/2 sdt kunyit bubuk</li>
				<li> 2 lembar daun jeruk</li>
				<li> Secukupnya garam</li>
				<li> Secukupnya merica</li>
				<li>Secukupnya kaldu ayam bubuk (opsional)</li>
				<li>Secukupnya minyak goreng</li>
			</ul>

			<br>
			<h3>Bahan halus:</h3>
			<ol>
				<li> 5 siung bawang putih</li>
			    <li> 10 buah bawang merah</li>
			  <li>30 buah cabai kecil</li>
			</ol>



			<br>
			<h3>Cara Memasak:</h3>
			<ol>
				<li>Potong ayam sekecil mungkin. Cuci lalu rendam dengan air asam, diamkan sekitar 15 menit.</li>
			    <li>Tumis bumbu halus hingga harum, masukkan ayam termasuk juga air asamnya, aduk rata.</li>
			  <li>Masukkan semua bahan lainnya lalu aduk rata dan koreksi rasa.</li>
			  <li> Masak hingga airnya menyusut sambil tetap diaduk sesekali agar bumbu meresap merata.</li>
			  <li> Sajikan ayam nasu palekko bersama sepiring nasi hangat.</li>
			</ol>

