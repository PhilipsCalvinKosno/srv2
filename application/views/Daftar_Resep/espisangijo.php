				<h2>Es Pisang Ijo</h2>
				<p>Sajian khas Makassar ini, sesuai dengan namanya berupa bubur sumsum yang disajikan dengan pisang berbalut adonan berwarna hijau. Kemudian disiram santan, dikucuri sirup merah dan diberi es batu.

Meski berasal dari Makassar, tetapi es pisang ijo ini sangat populer di Jakarta dan beberapa kota besar lainnya. Bahkan saking populernya, es pisang ijo menjadi menu takjil untuk berbuka puasa yang selalu diburu banyak orang.

Dibalik kelezatan dan kepopulerannya, es pisang ijo menyimpan fakta menarik yang mungkin belum diketahui orang banyak. Seperti cerita awal mula tercipta, filosofi hingga jadi ikon kuliner Makassar.
				</p>

        <br>
				<h3>Bahan Kulit:</h3>
				<ul>
					<li>1 liter air</li>
				<li> 20 lembar daun pandan</li>
				  <li> 100 gram gula pasir</li>
				  <li> 400 gram tepung beras</li>
				  <li> 400 gram tepung terigu</li>
				  <li>1 liter santan kental</li>
				<li> 10 tetes Pewarna makanan hijau</li>
				</ul>

				<br>
				<h3>Bumbu Vla :</h3>
				<ul>
					<li>1 liter santan kental</li>
				  <li>100 gram tepung beras</li>
				  <li>daun pandan ikat simpul</li>
				<li>1 sdt garam</li>
				<li> 50 gram gula pasir</li>
				</ul>

				<br>
				<h3>Bahan tambahan:</h3>
				<ul>
					<li>sirup pisang ambon/sirup warna merah</li>
					<li> daun pisang untuk mengukus pisang ijonya</li>
					<li> es serut/es batu</li>
					<li>susu kental manis</li>
				</ul>

				<br>
				<h3>Cara Memasak:</h3>
				<ol>
					<li> Kukus pisang 10 menit. Tiriskan.</li>
					<li> Buat adonan kulit, cuci bersih pandan lalu potong kecil-kecil dan beri air. Blender, lalu saring. Ambil 1 liter air saringan pandan, sisihkan. Campur tepung beras dan terigu lalu ayak supaya halus, aduk sampai tidak menggumpal.</li>
					<li> Tambahkan gula, santan, serta pewarna makanan. Letakkan pada wadah tahan panas, kukus selama 30 menit. Angkat dan diamkan hingga uap panas hilang.</li>
					<li> Siapkan plastik es ukuran besar, olesi minyak. Ambil adonan 2 sdm, ratakan dan pipihkan. Ambil pisang kukus, balutkan pisang dengan adonan hijau. Bentuk lalu letakkan pada daun pisang bersih. Olesi minyak agar tidak lengket. Gulung dan sematkan lidi. Kukus lagi selama 25 menit. Angkat dan dinginkan.</li>
					<li> Cara membuat vla, campur semua bahan vla hingga lembut, lalu saring. Masukkan ke panci, masak dengan api kecil sampai jadi bubur sumsum.</li>
					  <li>Letakkan pisang ijo di piring, tambahkan vla, es batu dan susu kental manis.</li>
				</ol>

