			<h2>Jalangkote</h2>
			<p>Jalangkote adalah makanan ringan kuliner khas Makassar yang bentuknya serupa dengan kue pastel. Bedanya kue pastel memiliki kulit yang lebih tebal dibandingkan jalangkote dan bila pastel dimakan bersama cabe rawit, jalangkote dimakan bersama sambal cair campuran cuka dan cabe.
			</p>

			<br>
			<h3>Bahan Kulit:</h3>
			<ul>
				<li>li>500 gram tepung terigu</li>
			<li>3 sdm minyak goreng</li>
				<li>4 sdm margarin, lelehkan</li>
				<li>1 sdt garam</li>
				<li>150 ml air</li>
			</ul>



                    <br>
                    <h3>Bumbu Isi:</h3>
                    <ul>
											<li>100 gram tauge, siangi</li>
											<li> 150 gram kentang, potong dadu</li>
											<li> 150 gram wortel, potong dadu</li>
											<li>2 batang daun bawang, iris tipis</li>
											<li>200 gram daging ayam, cincang</li>
											<li>4 butir telur rebus, potong jadi 8 bagian</li>
											<li>4 sdm minyak goreng</li>
											<li> 50 gram soun, seduh air lalu tiriskan</li>
											<li> 50 ml air</li>
											<li>6 siung bawang putih, iris tipis</li>
											<li>8 butir bawang merah, iris tipis</li>
											<li> 1 sdt lada bubuk</li>
											<li>1/2 sdt garam</li>
											<li>1/2 sdm gula pasir</li>
											<li>1/2 sdt pala bubuk</li>
                    </ul>



					<br>
					<h3>Cara Memasak:</h3>
					<ol>
						<li> Campur semua bahan kulit lalu uleni sampai kalis</li>
					  <li> Pipihkan adonan kulit hingga berbentuk bulat tipis. Sisihkan adonan kulit untuk digunakan nanti.</li>
					  <li> Untuk isinya panaskan minyak. Tumis bawang putih dan bawang merah hingga harum.</li>
					  <li>Lalu masukkan daging ayam, kentang, wortel dan daun bawang. Aduk rata.</li>
					<li>Tuangkan air. Aduk lalu masak hingga setengah matang.</li>
					  <li> Masukkan tauge, soun, garam, gula, lada bubuk, dan pala bubuk. Aduk rata lalu masak hingga matang. Koreksi rasa dan angkat.</li>
					  <li>Ambil satu lembar kulit dan beri 2 sendok makan isian beserta potongan telur.</li>
					<li> Bungkus menjadi bentuk setengah lingkaran dan rekatkan sisinya dengan putih telur.</li>
					  <li> Pilin sisinya yang telah melekat hingga bentuknya jadi keriting seperti pastel.</li>
					<li> Lakukan hingga semua adonan kulit dan isian habis.</li>
					<li> Siapkan penggorengan dan goreng jalangkote hingga matang. Angkat dan tiriskan.</li>
					<li>Jalangkote siap disajikan.</li>
					</ol>

