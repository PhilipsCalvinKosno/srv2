			<h2>Mie titi</h2>
			<p>Mie titi merupakan salah satu olahan mie yang berasal dari Makassar. Meskipun mungkin anda tidak terlalu mendengar mie titi jika dibandingkan mie-mie lainya, ternyata kepopuleran mie titi di kota asalnya, yakni Makassar itu sangatlah besar. Menurut sejarah sendiri mie titi ini diciptakan oleh seorang keturunan tiongkok bernama Ang Kho Tjao. Resep mie titi ini kemudian diturunkan kepada anak-anaknya dan mulai terkenal dan dikenal oleh masyarakat luas pada tahun 1970.
			</p>

			<br>
					<h3>Bahan Mie:</h3>
					<ul>
						<li>300 gr mie kering, rebus sebentar jangan over cook</li>
					    <li>Minyak secukupnya untuk menggoreng</li>
					</ul>

                    <br>
					<h3>Bahan Kuah:</h3>
					<ul>
						<li>500 ml kaldu ayam/sapi</li>
						<li> 200 gr daging ayam rebus (bisa diganti daging lain)</li>
						<li> 100 gr udang, kupas</li>
						<li> 5 buah bakso, potong-potong</li>
						<li> 3 buah sosis, iris (opsional)</li>
						<li> 2 buah wortel, iris</li>
						<li>1 bonggol sawi, potong</li>
						<li>1 butir telur, kocok lepas</li>
						<li> 3 siung bawang putih, cincang halus</li>
						<li> 1 siung bombai, cincang</li>
						<li> 2 btg daun bawang, iris halus</li>
						<li> 2 sdm minyak untuk menumis</li>
							<li> 1 sdm sagu, larutkan dalam air (bisa pakai maizena)</li>
						<li> 1 sdm kecap ikan</li>
						<li> 1 sdm saus tiram</li>
						<li> 1 sdt hatam (opsional)</li>
						<li> 1 sdt garam</li>
						<li> 1/2 sdt merica bubuk</li>
							<li> 1/2 sdt gula pasir (bisa ganti dengan sedikit saja penyedap jamur)</li>
					</ul>



					<br>
					<h3>Cara Memasak:</h3>
					<ol>
						<li> Panaskan minyak dengan api sedang, tata mie yang sudah direbus dalam saringan minyak. Goreng hingga kering dan matang. Angkat.</li>
						<li> Panaskan 2 sdm minyak, tumis bawang putih dan bombai hingga wangi dan layu.</li>
						  <li> Masukkan potongan daging, udang, masak hingga udang berubah warna.</li>
						<li> Masukkan sosis, bakso, wortel, aduk rata.</li>
						<li> Masukkan air kaldu, tambahkan kecap ikan, saus tiram, gula, garam dan merica.</li>
						  <li> Masak hingga wortel lunak. Masukkan sisa sayuran, aduk rata.</li>
						<li> Tambahkan larutan air sagu sedikit atau sesuaikan kekentalan yang diinginkan. Masak hingga kaldu meletup-letup.</li>
						<li> Masukkan kocokan telur. Sambil diaduk agar menyatu dengan kuah kaldu.</li>
						  <li> Terakhir tambahkan daun bawang, aduk rata. Koreksi rasa. Jika sudah pas, matikan api. Siap disajikan.</li>
						</ol>

